# Checking if a character is in a string
message = "Hello, World!"
if "H" in message:
    print("H is in the message")  # This will execute

# Checking if a key is in a dictionary
person = {"name": "John", "age": 30}
if "name" in person:
    print(f"Name: {person['name']}")  # This will execute

# Checking if a value is not in a list
fruits = ["apple", "banana", "cherry"]
if "orange" not in fruits:
    print("Orange is not in the list")  # This will execute